cells.append(md(["## 2. Custom Gym Environment"]))
cells.append(code([
    "import gymnasium as gym\n",
    "import numpy as np\n",
    "import pybullet as p\n",
    "from gymnasium import spaces\n",
    "from scipy.spatial.transform import Rotation\n",
    "\n",
    "class UR5eGymEnv(gym.Env):\n",
    "    metadata = {'render_modes': ['rgb_array']}\n",
    "    MAX_STEPS, PICK_THRESHOLD, ACTION_SCALE = 200, 0.05, 0.03\n",
    "    HOME_POSE = [0, -1.5708, 1.5708, -1.5708, -1.5708, 0]\n",
    "    CURRICULUM = [{'name':'easy','after':0},{'name':'medium','after':50000},{'name':'hard','after':150000}]\n",
    "\n",
    "    def __init__(self, gui=False, curriculum=True):\n",
    "        super().__init__()\n",
    "        import os\n",
    "        self._env = UR5eEnvironment(gui=gui and 'COLAB_GPU' not in os.environ)\n",
    "        self._validator = WorkspaceValidator()\n",
    "        self._curriculum = curriculum\n",
    "        self._global_steps = self._curriculum_stage = self._steps = 0\n",
    "        self._prev_dist = None\n",
    "        self._prev_action = np.zeros(3)\n",
    "        self._q_current = list(self.HOME_POSE)\n",
    "        self._dq_current = [0.0]*6\n",
    "        self._picked = False\n",
    "        # Obs 25D: q(6)+dq(6)+ee_pos(3)+ee_quat(4)+obj_pos(3)+rel(3)\n",
    "        self.observation_space = spaces.Box(-10.0, 10.0, (25,), np.float32)\n",
    "        self.action_space = spaces.Box(-1.0, 1.0, (3,), np.float32)\n",
    "\n",
    "    def _get_obs(self):\n",
    "        q = np.array(self._q_current, dtype=np.float32)\n",
    "        dq = np.array(self._dq_current, dtype=np.float32)\n",
    "        fk = forward_kinematics(self._q_current)\n",
    "        ee_w = np.array(local_to_world(fk['position']), dtype=np.float32)\n",
    "        ee_quat = Rotation.from_euler('zyx',[fk['euler'][2],fk['euler'][1],fk['euler'][0]]).as_quat().astype(np.float32)\n",
    "        obj = np.array(self._env.get_object_pose()[0], dtype=np.float32)\n",
    "        return np.concatenate([q, dq, ee_w, ee_quat, obj, obj-ee_w])\n",
    "\n",
    "    def _compute_reward(self, action):\n",
    "        fk = forward_kinematics(self._q_current)\n",
    "        ee_w = np.array(local_to_world(fk['position']))\n",
    "        obj = np.array(self._env.get_object_pose()[0])\n",
    "        dist = float(np.linalg.norm(ee_w - obj))\n",
    "        r = -dist*2.0 + (self._prev_dist-dist)*5.0 - 0.1\n",
    "        r -= float(np.linalg.norm(action-self._prev_action))*0.05\n",
    "        r -= float(np.linalg.norm(action))*0.1\n",
    "        self._prev_dist = dist\n",
    "        if dist < self.PICK_THRESHOLD:\n",
    "            r += 50.0; self._picked = True\n",
    "        ok, _ = self._validator.is_valid_ee(ee_w.tolist())\n",
    "        if not ok: r -= 2.0\n",
    "        return float(np.clip(r, -10.0, 55.0))\n",
    "\n",
    "    def reset(self, seed=None, options=None):\n",
    "        super().reset(seed=seed)\n",
    "        if self._curriculum:\n",
    "            for i,s in enumerate(self.CURRICULUM):\n",
    "                if self._global_steps >= s['after']: self._curriculum_stage = i\n",
    "        self._steps = 0; self._picked = False\n",
    "        self._prev_action = np.zeros(3)\n",
    "        self._q_current = list(self.HOME_POSE); self._dq_current = [0.0]*6\n",
    "        self._env.reset()\n",
    "        obs = self._get_obs()\n",
    "        self._prev_dist = float(np.linalg.norm(obs[18:21]))\n",
    "        return obs, {'stage': self.CURRICULUM[self._curriculum_stage]['name']}\n",
    "\n",
    "    def step(self, action):\n",
    "        self._steps += 1; self._global_steps += 1\n",
    "        dx,dy,dz = np.clip(action,-1,1) * self.ACTION_SCALE\n",
    "        fk = forward_kinematics(self._q_current)\n",
    "        ee_w = local_to_world(fk['position'])\n",
    "        w_new = [ee_w[0]+dx, ee_w[1]+dy, ee_w[2]+dz]\n",
    "        safe = self._validator.clamp_to_workspace(w_new)\n",
    "        l_new = world_to_local(safe)\n",
    "        ik = inverse_kinematics(l_new, list(fk['euler']), q_current=self._q_current)\n",
    "        ik_ok = False\n",
    "        if ik['best'] is not None:\n",
    "            q_new = ik['best']\n",
    "            self._dq_current = [(q_new[i]-self._q_current[i])*60 for i in range(6)]\n",
    "            self._q_current = q_new\n",
    "            self._env.set_joint_positions(self._q_current); ik_ok = True\n",
    "        else:\n",
    "            self._dq_current = [0.0]*6\n",
    "        self._env.step(5)\n",
    "        reward = self._compute_reward(action)\n",
    "        if not ik_ok: reward = float(np.clip(reward-2.0, -10.0, 55.0))\n",
    "        self._prev_action = action.copy()\n",
    "        return (self._get_obs(), reward, self._picked, self._steps>=self.MAX_STEPS,\n",
    "                {'picked':self._picked,'ik_success':ik_ok,'stage':self.CURRICULUM[self._curriculum_stage]['name']})\n",
    "\n",
    "    def render(self): return None\n",
    "    def close(self): self._env.close()\n",
    "\n",
    "# Verify\n",
    "from stable_baselines3.common.env_checker import check_env\n",
    "_t = UR5eGymEnv(); check_env(_t, warn=True); _t.close()\n",
    "print('\u2705 Environment check passed!')"
]))

