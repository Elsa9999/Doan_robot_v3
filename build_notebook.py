    "    ckpt_dir  = f'{BASE}/models/{algo}/seed{seed}'\n",
    "    norm_path = f'{BASE}/norm/{algo}_norm_s{seed}.pkl'\n",
    "    ckpts = sorted(glob.glob(f'{ckpt_dir}/*.zip'))\n",
    "    if not ckpts:\n",
    "        print(f'\u274c No checkpoint for {algo} seed{seed}'); return\n",
    "    latest = ckpts[-1]\n",
    "    print(f'\ud83d\udcc2 Loading: {os.path.basename(latest)}')\n",
    "\n",
    "    def make_env(): return UR5eGymEnv(gui=False, curriculum=True)\n",
    "    try: vec = SubprocVecEnv([make_env]*N_ENVS)\n",
    "    except: vec = DummyVecEnv([make_env]*N_ENVS)\n",
    "\n",
    "    if os.path.exists(norm_path):\n",
    "        vec = VecNormalize.load(norm_path, vec); vec.training = True\n",
    "    else:\n",
    "        vec = VecNormalize(vec)\n",
    "\n",
    "    model = ModelClass.load(latest, env=vec, device='cuda')\n",
    "    remaining = total_steps - steps_done\n",
