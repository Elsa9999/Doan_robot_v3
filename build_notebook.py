    "from stable_baselines3 import SAC, PPO\n",
    "import json\n",
    "\n",
    "def calc_success_rate(model, norm_path, n=50):\n",
    "    raw = UR5eGymEnv(gui=False)\n",
    "    ev = VecNormalize.load(norm_path, DummyVecEnv([lambda: raw]))\n",
    "    ev.training = False; ev.norm_reward = False\n",
    "    ok = 0\n",
    "    for _ in range(n):\n",
    "        obs = ev.reset(); done = False\n",
    "        while not done:\n",
    "            a, _ = model.predict(obs, deterministic=True)\n",
    "            obs, _, done, info = ev.step(a)\n",
    "            if isinstance(done, np.ndarray): done = done[0]\n",
    "        if info[0].get('picked', False): ok += 1\n",
    "    ev.close()\n",
    "    return ok/n*100\n",
    "\n",
    "all_results = {}\n",
    "for algo, Cls in [('SAC',SAC),('PPO',PPO)]:\n",
    "    rews, srs = [], []\n",
    "    for seed in SEEDS:\n",
    "        m = Cls.load(f'{BASE}/models/{algo.lower()}/{algo.lower()}_s{seed}_final', device='cuda')\n",
    "        np_ = f'{BASE}/norm/{algo.lower()}_norm_s{seed}.pkl'\n",
    "        sr = calc_success_rate(m, np_, n=50); srs.append(sr)\n",
    "        raw = UR5eGymEnv(gui=False)\n",
    "        ev = VecNormalize.load(np_, DummyVecEnv([lambda: raw]))\n",
    "        ev.training = ev.norm_reward = False\n",
